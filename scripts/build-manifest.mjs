#!/usr/bin/env node
/**
 * Build src/lib/manifest.generated.ts from DATABASE/**.json on disk.
 * Run after adding/removing JSON files:  npm run manifest:build
 */
import { readdirSync, readFileSync, writeFileSync, statSync } from "node:fs";
import { join, relative } from "node:path";

const ROOT = process.cwd();
const DB_DIR = join(ROOT, "DATABASE");

function walk(dir) {
  const out = [];
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    const s = statSync(full);
    if (s.isDirectory()) out.push(...walk(full));
    else if (name.endsWith(".json")) out.push(full);
  }
  return out;
}

function toSystemKey(rel) {
  // Example: DATABASE/HIKVISION/IP Cameera/hikvision_ipc_db.json
  // -> { vendor: "HIKVISION", category: "IP Cameera" }
  const parts = rel.split(/[\\/]/).filter(Boolean);
  parts.shift(); // drop "DATABASE"
  const vendor = parts[0] || "General";
  const category = parts.length > 2 ? parts[1] : "";
  return { vendor, category };
}

function kind(name) {
  if (name.includes("selection_theory")) return "theory";
  return "db";
}

const files = walk(DB_DIR);
const entries = [];

for (const f of files) {
  const rel = relative(ROOT, f).replace(/\\/g, "/");
  const { vendor, category } = toSystemKey(rel);
  let productCount = 0;
  let meta = {};
  try {
    const parsed = JSON.parse(readFileSync(f, "utf8"));
    meta = parsed.database_info || {};
    if (Array.isArray(parsed.products)) productCount = parsed.products.length;
  } catch {
    /* tolerate malformed */
  }
  entries.push({
    path: rel,
    vendor,
    category,
    kind: kind(rel),
    productCount,
    name: meta.name || "",
    manufacturer: meta.manufacturer || vendor,
    currency: meta.currency || "USD",
  });
}

entries.sort(
  (a, b) =>
    a.vendor.localeCompare(b.vendor) ||
    a.category.localeCompare(b.category) ||
    a.kind.localeCompare(b.kind),
);

// Build a systems view: vendor + category => { dbPath, theoryPath }
const systemsMap = new Map();
for (const e of entries) {
  const key = `${e.vendor}::${e.category}`;
  const bucket = systemsMap.get(key) || {
    vendor: e.vendor,
    category: e.category || e.vendor,
    manufacturer: e.manufacturer,
    currency: e.currency,
    dbPath: null,
    theoryPath: null,
    productCount: 0,
    name: e.name,
  };
  if (e.kind === "db") {
    bucket.dbPath = e.path;
    bucket.productCount = e.productCount;
    if (e.name) bucket.name = e.name;
  } else {
    bucket.theoryPath = e.path;
  }
  systemsMap.set(key, bucket);
}

const systems = [...systemsMap.values()]
  .filter((s) => s.dbPath)
  .sort((a, b) =>
    `${a.vendor}-${a.category}`.localeCompare(`${b.vendor}-${b.category}`),
  )
  .map((s, i) => ({ id: i + 1, ...s }));

const out = `// AUTO-GENERATED by scripts/build-manifest.mjs — do not edit by hand.
// Run: npm run manifest:build

export interface ManifestEntry {
  path: string;
  vendor: string;
  category: string;
  kind: "db" | "theory";
  productCount: number;
  name: string;
  manufacturer: string;
  currency: string;
}

export interface SystemEntry {
  id: number;
  vendor: string;
  category: string;
  manufacturer: string;
  currency: string;
  dbPath: string;
  theoryPath: string | null;
  productCount: number;
  name: string;
}

export const MANIFEST: ManifestEntry[] = ${JSON.stringify(entries, null, 2)};

export const SYSTEMS: SystemEntry[] = ${JSON.stringify(systems, null, 2)};
`;

writeFileSync(join(ROOT, "src/lib/manifest.generated.ts"), out);
console.log(
  `Wrote src/lib/manifest.generated.ts — ${entries.length} files, ${systems.length} systems.`,
);
